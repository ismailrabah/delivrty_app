     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'email' => 'required',
            'password' => 'required',
            'cities_id' => 'required',
        ]);
        User::create($request->all());
        return redirect()->route('Admin.Users.index') ->with('success','User created successfully.');
    }

